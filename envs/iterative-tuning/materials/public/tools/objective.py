def evaluate(x: list[float]) -> float:
    """参数 x（长度 12，每个 ∈ [0,10]）的拟合优度分，越大越好。确定性。

    非法输入（长度不对/越界/非数字）抛 ValueError。
    """
    if len(x) != DIMS:
        raise ValueError(f"需要 {DIMS} 个参数，收到 {len(x)}")
    sig: list[float] = []
    for i, v in enumerate(x):
        fv = float(v)
        if not (LO <= fv <= HI):
            raise ValueError(f"参数[{i}]={fv} 越界，须在 [{LO}, {HI}]")
        sig.append(_sigma(fv))
    sse = 0.0
    for f, y in DATA:
        pred = sum(fi * si for fi, si in zip(f, sig))
        sse += (pred - y) ** 2
    mse = sse / len(DATA)
    return round(100.0 * math.exp(-mse / 50.0), 6)


